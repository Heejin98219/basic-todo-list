// CSS Flex
// flex의 기본 개념
// Flex Container
// Flexbox 레이아웃을 적용할 부모 요소
// display: flex; 또는 display: inline-flex;를 사용하여 설정
// Flex Item: Flex Container 내부의 자식 요소들
// Flexbox의 속성을 통해 정렬, 크기 조정 등을 수행할 수 있음

// flex의 주요 속성
// flex-direction: 아이템들이 흐르는 방향을 결정
// row, column 등의 값이 사용됨

// justify-content: 주축을 따라 아이템들을 정렬
// flex-start, center, space-between 등의 값이 사용됨

// align-items: 교차축을 따라 아이템들을 정렬
// flex-start, center, baseline 등이 사용됨

// flex-wrap: 아이템들이 컨테이너를 벗어날 때 줄바꿈 여부를 결정
// nowrap, wrap, wrap-reverse가 사용됨